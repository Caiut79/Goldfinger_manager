  protected getAttendanceDayListCopy(day: AttendanceCalendarDay): string {
    if (day.entry) {
      if (day.entry.entryType === 'ferie') {
        return 'Ferie';
      }

      if (day.entry.entryType === 'malattia') {
        return 'Malattia';
      }

      if (day.entry.entryType === 'festivo') {
        return 'Chiuso';
      }

      return `${this.getAttendanceTypeLabel(day.entry.entryType)} · ${this.formatCompactHours(day.entry.workedHours)}h`;
    }

    if (day.isCompanyClosed) {
      return 'Chiuso';
    }

    if (day.isNationalHoliday) {
      return 'Festivo nazionale';
    }

    if (day.plannedShift?.isWorking) {
      return `${this.formatCompactHours(day.plannedShift.hours)}h previste`;
    }

    return 'Non lavorativo';
  }

