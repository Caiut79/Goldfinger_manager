  protected toggleCompanyClosureDay(day: WeekdayKey): void {
    this.appSettings.update((settings) => {
      const daySet = new Set(settings.companyClosureDays);

      if (daySet.has(day)) {
        daySet.delete(day);
      } else {
        daySet.add(day);
      }

      return {
        companyClosureDays: [...daySet],
      };
    });

    this.staffForm.weeklySchedule = this.normalizeWeeklyScheduleForm(this.staffForm.weeklySchedule);
  }

  protected isCompanyClosureDay(day: WeekdayKey): boolean {
    return this.appSettings().companyClosureDays.includes(day);
  }

  protected toggleStaffWorkingDay(day: WeekdayKey): void {
    if (this.isCompanyClosureDay(day)) {
      return;
    }

    const currentDay = this.staffForm.weeklySchedule[day];
    this.staffForm.weeklySchedule = {
      ...this.staffForm.weeklySchedule,
      [day]: {
        isWorking: !currentDay.isWorking,
        hours: !currentDay.isWorking ? this.normalizeAmount(this.staffForm.defaultDailyHours) : 0,
      },
    };
  }

  protected updateStaffWorkingHours(day: WeekdayKey, hours: number): void {
    const normalizedHours = Math.max(0, this.normalizeAmount(hours));
    this.staffForm.weeklySchedule = {
      ...this.staffForm.weeklySchedule,
      [day]: {
        isWorking: !this.isCompanyClosureDay(day) && normalizedHours > 0,
        hours: this.isCompanyClosureDay(day) ? 0 : normalizedHours,
      },
    };
  }

  protected async hideOrDeleteStaffMember(staffMember: StaffMember): Promise<void> {
    if (this.hasStaffSales(staffMember.id)) {
      if (!window.confirm(`Nascondere ${staffMember.fullName}? Rimarra nei report storici ma non comparira piu in incasso.`)) {
        return;
      }

      await this.storeService.updateStaffMemberVisibility(staffMember.id, false);
      this.staffMembers.update((members) =>
        members.map((member) => (member.id === staffMember.id ? { ...member, isActive: false } : member)),
      );
      this.ensureSelectedStaffMember();
      this.setFeedback('info', 'Personale nascosto con successo.');
      return;
    }

    if (!window.confirm(`Eliminare ${staffMember.fullName}?`)) {
      return;
    }

    await this.storeService.deleteStaffMember(staffMember.id);
    this.staffMembers.update((members) => members.filter((member) => member.id !== staffMember.id));
    this.ensureSelectedStaffMember();
    this.setFeedback('info', 'Personale eliminato.');
  }

  protected async restoreStaffMember(staffMember: StaffMember): Promise<void> {
    if (staffMember.role === 'titolare' && this.ownerCount() >= 1) {
      this.setFeedback('error', 'Hai gia una titolare attiva.');
      return;
    }

    if (staffMember.role === 'dipendente' && this.employeeCount() >= 2) {
      this.setFeedback('error', 'Hai gia due dipendenti attivi.');
      return;
    }

    await this.storeService.updateStaffMemberVisibility(staffMember.id, true);
    this.staffMembers.update((members) =>
      members.map((member) => (member.id === staffMember.id ? { ...member, isActive: true } : member)),
    );
    this.ensureSelectedStaffMember();
    this.setFeedback('success', 'Personale riattivato.');
  }

  protected async addDailySale(): Promise<void> {
    if (!this.saleForm.staffMemberId) {
      this.setFeedback('error', 'Seleziona prima la titolare o una dipendente.');
      return;
    }

    if (!this.saleForm.saleDate) {
      this.setFeedback('error', 'Inserisci la data dell\'incasso.');
      return;
    }

    if (this.saleForm.regularAmount <= 0 && this.saleForm.testAmount <= 0) {
      this.setFeedback('error', 'Inserisci almeno un importo maggiore di zero.');
      return;
    }

    this.isSavingSale.set(true);

    try {
      const createdSale = await this.storeService.createDailySale({
        saleDate: this.saleForm.saleDate,
        staffMemberId: this.saleForm.staffMemberId,
        regularAmount: this.normalizeAmount(this.saleForm.regularAmount),
        testAmount: this.normalizeAmount(this.saleForm.testAmount),
        notes: this.saleForm.notes,
      });

      this.dailySales.update((sales) => [...sales, createdSale]);
      this.saleForm.regularAmount = 0;
      this.saleForm.testAmount = 0;
      this.saleForm.notes = '';
      this.setFeedback('success', 'Incasso giornaliero salvato.');
    } finally {
      this.isSavingSale.set(false);
    }
  }

  protected onAttendanceStaffChange(): void {
    const selectedStaffMember = this.staffMemberMap().get(this.attendanceForm.staffMemberId);

    if (!selectedStaffMember) {
      return;
    }

    const plannedShift = this.getPlannedShiftForDate(selectedStaffMember, this.attendanceForm.entryDate);
    this.attendanceForm.workedHours = plannedShift?.hours ?? 0;
  }

  protected selectAttendanceDay(isoDate: string): void {
    if (this.attendanceFilter.staffMemberId) {
      this.attendanceForm.staffMemberId = this.attendanceFilter.staffMemberId;
    }

    this.attendanceForm.entryDate = isoDate;
    this.attendanceForm.endDate = isoDate;

    const existingEntry = this.explicitAttendanceEntriesForSelectedStaff().find((entry) => entry.entryDate === isoDate);

    if (existingEntry) {
      this.startEditAttendanceEntry(existingEntry);
      this.isAttendanceModalOpen.set(true);
      return;
    }

    this.editingAttendanceEntryId.set(null);
    const selectedStaffMember = this.getSelectedAttendanceStaffMember();
    const plannedShift = selectedStaffMember ? this.getPlannedShiftForDate(selectedStaffMember, isoDate) : null;
    this.attendanceForm.entryType = !plannedShift || !plannedShift.isWorking ? 'festivo' : 'lavoro';
    this.attendanceForm.notes = '';
    this.attendanceForm.workedHours = plannedShift?.hours ?? 0;
    this.isAttendanceModalOpen.set(true);
  }

