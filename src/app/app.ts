  protected readonly reportRows = computed<StaffPerformance[]>(() => {
    const salesByStaffMember = new Map<string, DailySale[]>();

    for (const sale of this.filteredReportSales()) {
      const collection = salesByStaffMember.get(sale.staffMemberId) ?? [];
      collection.push(sale);
      salesByStaffMember.set(sale.staffMemberId, collection);
    }

    return this.staffMembers()
      .map((staffMember) => {
        const sales = salesByStaffMember.get(staffMember.id) ?? [];
        const totalRegular = sales.reduce((total, sale) => total + sale.regularAmount, 0);
        const totalTest = sales.reduce((total, sale) => total + sale.testAmount, 0);
        const workedDays = new Set(sales.map((sale) => sale.saleDate)).size;
        const totalAmount = totalRegular + totalTest;

        return {
          staffMember,
          totalRegular,
          totalTest,
          totalAmount,
          daysWorked: workedDays,
          averageDaily: workedDays > 0 ? totalAmount / workedDays : 0,
        };
      })
      .filter((row) => row.staffMember.isActive || row.totalAmount > 0)
      .sort((left, right) => right.totalAmount - left.totalAmount);
  });

