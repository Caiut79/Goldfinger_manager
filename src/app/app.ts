  protected async submitAuth(): Promise<void> {
    const email = this.authForm.email.trim().toLowerCase();
    const password = this.authForm.password;
    const confirmPassword = this.authForm.confirmPassword;

    if (!email) {
      this.setFeedback('error', 'Inserisci l\'email.');
      return;
    }

    if (!password) {
      this.setFeedback('error', 'Inserisci la password.');
      return;
    }

    if (this.authMode() === 'sign-up' && password !== confirmPassword) {
      this.setFeedback('error', 'Password e conferma password non coincidono.');
      return;
    }

    this.isAuthProcessing.set(true);

    try {
      if (this.authMode() === 'sign-up') {
        const result = await this.storeService.signUp(email, password);

        if (result.needsEmailConfirmation) {
          this.authForm.password = '';
          this.authForm.confirmPassword = '';
          this.authMode.set('sign-in');
          this.showAuthPassword.set(false);
          this.showAuthConfirmPassword.set(false);
          this.setFeedback('info', 'Account creato. Se non entri subito, controlla le impostazioni email di Supabase.');
          return;
        }

        this.showAuthPassword.set(false);
        this.showAuthConfirmPassword.set(false);
        this.setFeedback('success', 'Account creato e accesso effettuato.');
        return;
      }

      await this.storeService.signIn(email, password);
      this.authForm.password = '';
      this.authForm.confirmPassword = '';
      this.showAuthPassword.set(false);
      this.showAuthConfirmPassword.set(false);
      this.setFeedback('success', 'Accesso effettuato.');
    } catch (error) {
      this.setFeedback('error', this.getErrorMessage(error, 'Impossibile completare l\'accesso.'));
    } finally {
      this.isAuthProcessing.set(false);
    }
  }

