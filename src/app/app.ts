  private getWeekdayKeyFromDate(isoDate: string): WeekdayKey {
    const [yearValue, monthValue, dayValue] = isoDate.split('-');
    const year = Number.parseInt(yearValue, 10);
    const month = Number.parseInt(monthValue, 10);
    const day = Number.parseInt(dayValue, 10);
    const date = new Date(year, month - 1, day);
    const weekdayMap: WeekdayKey[] = ['domenica', 'lunedi', 'martedi', 'mercoledi', 'giovedi', 'venerdi', 'sabato'];
    return weekdayMap[date.getDay()];
  }

  private getPlannedShiftForDate(staffMember: StaffMember, isoDate: string) {
    const weekdayKey = this.getWeekdayKeyFromDate(isoDate);

    if (this.isCompanyClosureDay(weekdayKey) || this.isNationalHolidayDate(isoDate)) {
      return {
        isWorking: false,
        hours: 0,
      };
    }

    return staffMember.weeklySchedule[weekdayKey];
  }

