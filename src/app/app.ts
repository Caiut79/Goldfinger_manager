  private async initializeApp(): Promise<void> {
    this.authSubscription = this.storeService.onAuthStateChange((session) => {
      void this.syncAuthSession(session);
    });

    try {
      const session = await this.storeService.getSession();
      await this.syncAuthSession(session);
    } catch (error) {
      this.isLoading.set(false);
      this.isAuthReady.set(true);
      this.setFeedback('error', this.getErrorMessage(error, 'Impossibile avviare il login.'));
    }
  }

  private async syncAuthSession(session: Session | null): Promise<void> {
    this.authSession.set(session);
    this.isAuthReady.set(true);

    if (!session) {
      this.loadedUserId = null;
      this.loadingUserId = null;
      this.clearDataState();
      this.isLoading.set(false);
      return;
    }

    if (this.loadedUserId === session.user.id || this.loadingUserId === session.user.id) {
      return;
    }

    this.loadingUserId = session.user.id;

    try {
      await this.loadData();
      this.loadedUserId = session.user.id;
    } finally {
      if (this.loadingUserId === session.user.id) {
        this.loadingUserId = null;
      }
    }
  }

  private async loadData(): Promise<void> {
    this.isLoading.set(true);

    try {
      const [appSettings, staffMembers, dailySales, attendanceEntries] = await Promise.all([
        this.storeService.getAppSettings(),
        this.storeService.getStaffMembers(),
        this.storeService.getDailySales(),
        this.storeService.getAttendanceEntries(),
      ]);

      this.appSettings.set(appSettings);
      this.staffMembers.set(staffMembers);
      this.dailySales.set(dailySales);
      this.attendanceEntries.set(attendanceEntries);
      this.ensureSelectedStaffMember();
      this.ensureSelectedAttendanceStaffMember();
    } finally {
      this.isLoading.set(false);
    }
  }

