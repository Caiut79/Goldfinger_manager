  private getAttendanceEditEntries(entry: AttendanceEntry): AttendanceEntry[] {
    if (entry.entryType !== 'ferie' && entry.entryType !== 'malattia') {
      return [entry];
    }

    const candidateEntries = this.attendanceEntries()
      .filter(
        (candidateEntry) =>
          candidateEntry.staffMemberId === entry.staffMemberId &&
          candidateEntry.entryType === entry.entryType &&
          candidateEntry.notes === entry.notes,
      )
      .sort((left, right) => left.entryDate.localeCompare(right.entryDate));
    const entriesByDate = new Map(candidateEntries.map((candidateEntry) => [candidateEntry.entryDate, candidateEntry]));
    const groupedEntries: AttendanceEntry[] = [entry];
    let previousDate = entry.entryDate;
    let nextDate = entry.entryDate;

    while (true) {
      const candidateDate = this.getNearestWorkingDate(previousDate, -1);

      if (!candidateDate) {
        break;
      }

      const candidateEntry = entriesByDate.get(candidateDate);

      if (!candidateEntry) {
        break;
      }

      groupedEntries.unshift(candidateEntry);
      previousDate = candidateDate;
    }

    while (true) {
      const candidateDate = this.getNearestWorkingDate(nextDate, 1);

      if (!candidateDate) {
        break;
      }

      const candidateEntry = entriesByDate.get(candidateDate);

      if (!candidateEntry) {
        break;
      }

      groupedEntries.push(candidateEntry);
      nextDate = candidateDate;
    }

    return groupedEntries;
  }

  private getNearestWorkingDate(startDate: string, direction: -1 | 1): string | null {
    let cursorDate = startDate;

    for (let attempt = 0; attempt < 370; attempt += 1) {
      cursorDate = this.shiftIsoDate(cursorDate, direction);

      if (!this.isHolidayDate(cursorDate)) {
        return cursorDate;
      }
    }

    return null;
  }

  private shiftIsoDate(isoDate: string, dayOffset: number): string {
    const shiftedDate = new Date(isoDate);
    shiftedDate.setDate(shiftedDate.getDate() + dayOffset);
    return this.toInputDate(shiftedDate);
  }

  private isCompanyClosureDate(isoDate: string): boolean {
    return this.isCompanyClosureDay(this.getWeekdayKeyFromDate(isoDate));
  }

  private isNationalHolidayDate(isoDate: string): boolean {
    const fixedNationalHolidays = new Set([
      '01-01',
      '01-06',
      '04-25',
      '05-01',
      '06-02',
      '08-15',
      '11-01',
      '12-08',
      '12-25',
      '12-26',
    ]);
    const monthDay = isoDate.slice(5, 10);

    if (fixedNationalHolidays.has(monthDay)) {
      return true;
    }

    const easterMonday = this.getEasterMondayIsoDate(Number.parseInt(isoDate.slice(0, 4), 10));
    return isoDate === easterMonday;
  }

  private getEasterMondayIsoDate(year: number): string {
    const easterDate = this.getEasterDate(year);
    easterDate.setDate(easterDate.getDate() + 1);
    return this.toInputDate(easterDate);
  }

  private getEasterDate(year: number): Date {
    const century = Math.floor(year / 100);
    const yearInCentury = year % 100;
    const leapCenturyAdjust = Math.floor(century / 4);
    const centuryRemainder = century % 4;
    const correction = Math.floor((century + 8) / 25);
    const moonCorrection = Math.floor((century - correction + 1) / 3);
    const epact = (19 * (year % 19) + century - leapCenturyAdjust - moonCorrection + 15) % 30;
    const yearQuarter = Math.floor(yearInCentury / 4);
    const yearRemainder = yearInCentury % 4;
    const weekdayCorrection = (32 + 2 * centuryRemainder + 2 * yearQuarter - epact - yearRemainder) % 7;
    const monthAdjust = Math.floor((year % 19 + 11 * epact + 22 * weekdayCorrection) / 451);
    const month = Math.floor((epact + weekdayCorrection - 7 * monthAdjust + 114) / 31);
    const day = ((epact + weekdayCorrection - 7 * monthAdjust + 114) % 31) + 1;
    return new Date(year, month - 1, day);
  }

  private getWeeklyScheduleValidationError(weeklySchedule: WeeklyShiftTemplate): string | null {
    for (const weekday of this.weekdayOptions) {
      const dayConfig = weeklySchedule[weekday.key];

      if (this.isCompanyClosureDay(weekday.key) && dayConfig.isWorking) {
        return `Non puoi impostare ${weekday.label} come lavorativo se l'azienda e chiusa.`;
      }

      if (dayConfig.isWorking && dayConfig.hours <= 0) {
        return `Inserisci ore maggiori di zero per ${weekday.label}.`;
      }

      if (!dayConfig.isWorking && dayConfig.hours !== 0) {
        return `Per ${weekday.label} non lavorativo le ore devono essere zero.`;
      }
    }

    return null;
  }

  private normalizeWeeklyScheduleForm(weeklySchedule: WeeklyShiftTemplate): WeeklyShiftTemplate {
    const normalizedSchedule = this.cloneWeeklySchedule(weeklySchedule);

    for (const weekday of this.weekdayOptions) {
      if (this.isCompanyClosureDay(weekday.key)) {
        normalizedSchedule[weekday.key] = {
          isWorking: false,
          hours: 0,
        };
        continue;
      }

      normalizedSchedule[weekday.key] = {
        isWorking: normalizedSchedule[weekday.key].isWorking,
        hours: normalizedSchedule[weekday.key].isWorking
          ? this.normalizeAmount(normalizedSchedule[weekday.key].hours)
          : 0,
      };
    }

    return normalizedSchedule;
  }

  private cloneWeeklySchedule(weeklySchedule: WeeklyShiftTemplate): WeeklyShiftTemplate {
    return {
      lunedi: { ...weeklySchedule.lunedi },
      martedi: { ...weeklySchedule.martedi },
      mercoledi: { ...weeklySchedule.mercoledi },
      giovedi: { ...weeklySchedule.giovedi },
      venerdi: { ...weeklySchedule.venerdi },
      sabato: { ...weeklySchedule.sabato },
      domenica: { ...weeklySchedule.domenica },
    };
  }

