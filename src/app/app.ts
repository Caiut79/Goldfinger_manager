  protected async saveStaffMember(): Promise<void> {
    const fullName = this.staffForm.fullName.trim();
    const defaultDailyHours = this.normalizeAmount(this.staffForm.defaultDailyHours);
    const weeklySchedule = this.normalizeWeeklyScheduleForm(this.staffForm.weeklySchedule);

    if (!fullName) {
      this.setFeedback('error', 'Inserisci il nome della persona da registrare.');
      return;
    }

    if (defaultDailyHours < 0) {
      this.setFeedback('error', 'Le ore giornaliere non possono essere negative.');
      return;
    }

    const weeklyScheduleError = this.getWeeklyScheduleValidationError(weeklySchedule);

    if (weeklyScheduleError) {
      this.setFeedback('error', weeklyScheduleError);
      return;
    }

    const duplicateMember = this.staffMembers().find(
      (member) =>
        member.id !== this.editingStaffMemberId() && member.fullName.toLowerCase() === fullName.toLowerCase(),
    );

    if (duplicateMember) {
      this.setFeedback('error', 'Esiste gia un membro del personale con questo nome.');
      return;
    }

    if (
      this.staffForm.role === 'titolare' &&
      this.ownerCount(this.editingStaffMemberId()) >= 1
    ) {
      this.setFeedback('error', 'Puoi registrare una sola titolare.');
      return;
    }

    if (
      this.staffForm.role === 'dipendente' &&
      this.employeeCount(this.editingStaffMemberId()) >= 2
    ) {
      this.setFeedback('error', 'Hai indicato un massimo di due dipendenti.');
      return;
    }

    this.isSavingStaff.set(true);

    try {
      const editingStaffMemberId = this.editingStaffMemberId();

      if (editingStaffMemberId) {
        await this.storeService.updateStaffMemberSettings({
          staffMemberId: editingStaffMemberId,
          fullName,
          role: this.staffForm.role,
          defaultDailyHours,
          weeklySchedule,
        });
        this.staffMembers.update((members) =>
          members.map((member) =>
            member.id === editingStaffMemberId
              ? { ...member, fullName, role: this.staffForm.role, defaultDailyHours, weeklySchedule }
              : member,
          ),
        );
        this.setFeedback('success', 'Personale aggiornato correttamente.');
      } else {
        const createdStaffMember = await this.storeService.createStaffMember(
          fullName,
          this.staffForm.role,
          defaultDailyHours,
          weeklySchedule,
        );
        this.staffMembers.update((members) => [...members, createdStaffMember]);

        if (!this.saleForm.staffMemberId) {
          this.saleForm.staffMemberId = createdStaffMember.id;
        }

        if (!this.attendanceForm.staffMemberId && createdStaffMember.role === 'dipendente' && createdStaffMember.isActive) {
          this.attendanceForm.staffMemberId = createdStaffMember.id;
        }

        this.setFeedback('success', 'Personale registrato correttamente.');
      }

      this.resetStaffForm();
      this.ensureSelectedStaffMember();
      this.ensureSelectedAttendanceStaffMember();
    } finally {
      this.isSavingStaff.set(false);
    }
  }

