  protected readonly attendanceCalendarDays = computed(() => {
    this.attendanceFilterVersion();
    const year = Number.parseInt(this.attendanceFilter.year, 10);
    const monthIndex = Number.parseInt(this.attendanceFilter.month, 10) - 1;
    const firstDay = new Date(year, monthIndex, 1);
    const lastDay = new Date(year, monthIndex + 1, 0);
    const days: AttendanceCalendarDay[] = [];
    const selectedStaffMember = this.getAttendanceStaffMemberById(this.attendanceFilter.staffMemberId);

    for (let day = 1; day <= lastDay.getDate(); day += 1) {
      const currentDate = new Date(year, monthIndex, day);
      const isoDate = this.toInputDate(currentDate);
      const weekdayKey = this.getWeekdayKeyFromDate(isoDate);
      const plannedShift = selectedStaffMember ? this.getPlannedShiftForDate(selectedStaffMember, isoDate) : null;
      const isNationalHoliday = this.isNationalHolidayDate(isoDate);
      days.push({
        isoDate,
        dayNumber: day,
        weekdayLabel: new Intl.DateTimeFormat(this.locale, { weekday: 'short' }).format(currentDate),
        isWeekend: weekdayKey === 'sabato' || weekdayKey === 'domenica',
        entry:
          this.attendanceEntriesForSelectedStaff().find((entry) => entry.entryDate === isoDate) ?? null,
        weekdayKey,
        isCompanyClosed: this.isCompanyClosureDate(isoDate),
        isNationalHoliday,
        plannedShift,
      });
    }

    return {
      monthStartWeekday: (firstDay.getDay() + 6) % 7,
      days,
    };
  });

